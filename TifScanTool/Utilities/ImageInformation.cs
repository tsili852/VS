using System;

using Leadtools;

namespace MainDemo
{
   public class ImageInformation
   {
      public RasterImage Image;
      public string Name;

      public ImageInformation()
      {
         Image = null;
         Name = string.Empty;
      }

      public ImageInformation(RasterImage i, string n)
      {
         Image = i;
         Name = n;
      }

      public ImageInformation(RasterImage i)
      {
         Image = i;
         Name = "Untitled";
      }
   }
}
